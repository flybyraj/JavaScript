//Comparison Operator
let a = 5; //Number
let b = "5"; //String -> Number
console.log("a =" , a , "b =" , b);

console.log("a==b" , a==b); //true or false
console.log("a!=b" , a!=b);

//Stricter Version
console.log("a===b", a===b);
console.log("a!==b", a!==b);

//Less than Greater than
let c=5;
let d=3;
console.log("c =" , c , "d =" , d);

console.log("5 > 3 =" , c > d); // True False
console.log("5 < 3 =" , c < d);

